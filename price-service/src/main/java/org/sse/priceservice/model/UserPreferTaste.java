package com.sample;


public class UserPreferTaste {

  private long userId;
  private long tasteId;
  private long isPrefer;


  public long getUserId() {
    return userId;
  }

  public void setUserId(long userId) {
    this.userId = userId;
  }


  public long getTasteId() {
    return tasteId;
  }

  public void setTasteId(long tasteId) {
    this.tasteId = tasteId;
  }


  public long getIsPrefer() {
    return isPrefer;
  }

  public void setIsPrefer(long isPrefer) {
    this.isPrefer = isPrefer;
  }

}
